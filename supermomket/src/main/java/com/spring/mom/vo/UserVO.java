package com.spring.mom.vo;

public class UserVO {
	private String u_id;
	private String u_pw;
	private String u_name;
	private String u_nickname;
	private String u_address;
	private String u_pno;
	private String u_date;
	private String u_secession; // default('n'),
	private String u_role; // default('user'),
	private String u_email;

	public String getU_id() {
		return u_id;
	}

	public void setU_id(String u_id) {
		this.u_id = u_id;
	}

	public String getU_pw() {
		return u_pw;
	}

	public void setU_pw(String u_pw) {
		this.u_pw = u_pw;
	}

	public String getU_name() {
		return u_name;
	}

	public void setU_name(String u_name) {
		this.u_name = u_name;
	}

	public String getU_nickname() {
		return u_nickname;
	}

	public void setU_nickname(String u_nickname) {
		this.u_nickname = u_nickname;
	}

	public String getU_address() {
		return u_address;
	}

	public void setU_address(String u_address) {
		this.u_address = u_address;
	}

	public String getU_pno() {
		return u_pno;
	}

	public void setU_pno(String u_pno) {
		this.u_pno = u_pno;
	}

	public String getU_date() {
		return u_date;
	}

	public void setU_date(String u_date) {
		this.u_date = u_date;
	}

	public String getU_secession() {
		return u_secession;
	}

	public void setU_secession(String u_secession) {
		this.u_secession = u_secession;
	}

	public String getU_role() {
		return u_role;
	}

	public void setU_role(String u_role) {
		this.u_role = u_role;
	}

	public String getU_email() {
		return u_email;
	}

	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
}
